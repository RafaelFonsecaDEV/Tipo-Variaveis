public class NumeroPositivoNegativo {
    public static void main(String[] args) {
        System.out.println("numeros positivos de 1 a 100");

        for (int positivo = 1; positivo <= 100; positivo++) {
            System.out.println("numero positivo: " + positivo);
        }

        System.out.println("numeros negativos de -1 a -100");
        for (int negativo = -1; negativo >= -100; negativo--) {
            System.out.println("numero nehgativo:" + negativo);
        }

    }
}
